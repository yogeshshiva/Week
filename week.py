num=int(input("enter your number between 1 to 7."))
if (num==1):
    print("sunday")
elif (num==2):
    print("monday")
elif (num==3):
    print("tuesday")
elif (num==4):
    print("wednesday")
elif (num==5):
    print("thrusday")
elif (num==6):
    print("friday")
elif (num==7):
    print("saturaday")
else:
    print("enter number only 1 to 7.")
